// Declaração das variáveis
let numeroA = 13;
let numeroB = 7;

// Operação de adição
let soma = numeroA + numeroB;
console.log(`Resultado da adição entre ${numeroA} e ${numeroB} é ${soma}`);

// Operação de subtração
let subtracao = numeroA - numeroB;
console.log(`Resultado da subtração entre ${numeroA} e ${numeroB} é ${subtracao}`);

// Operação de multiplicação
let multiplicacao = numeroA * numeroB;
console.log(`Resultado da multiplicação entre ${numeroA} e ${numeroB} é ${multiplicacao}`);

// Operação de divisão
let divisao = numeroA / numeroB;
console.log(`Resultado da divisão entre ${numeroA} e ${numeroB} é ${divisao}`);

// Operação módulo (resto)
let modulo = numeroA % numeroB;
console.log(`Resultado do módulo entre ${numeroA} e ${numeroB} é ${modulo}`);
